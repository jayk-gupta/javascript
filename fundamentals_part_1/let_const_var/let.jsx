// let
// The let declaration declares re-assignable, block-scoped local variables, optionally initializing each to a value.
// Variables declared with let are only accessible within the block (e.g., between curly braces { }) they are defined in, including loops and conditionals.
{
    let car = "swift"
    var Car = "swift"
}

console.log(Car); // work
